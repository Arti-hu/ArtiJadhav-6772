package Operators;

public class RelationalOp 
{

	public static void main(String[] args) 
	{
		int a=20;
		int b=20;
		
		System.out.println("Value of a is  "+a);
		System.out.println("Value of b is  "+b);
		
		System.out.println("a==b  " +(a==b));
		System.out.println("a<b  " +(a<b));
		System.out.println("a>b  " +(a>b));
		System.out.println("a!=b  " +(a!=b));
		System.out.println("a<=b  " +(a<=b));
		System.out.println("a>=b  " +(a>=b));

	}

}
